package com.model;

/**
 * 执法音视频关联文件EA_MEDIA_FILES
 * @author Administrator
 *
 */
public class MediaFiles {

	private String id;							  //音视频文件ID
	private String mediaId;								  //音视频记录ID
	private String fileSize;								  //文件大小
	private String fileType;                                 //文件格式
	private String filePath;                                 //文件地址
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMediaId() {
		return mediaId;
	}
	public void setMediaId(String mediaId) {
		this.mediaId = mediaId;
	}
	public String getFileSize() {
		return fileSize;
	}
	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}
	public String getFileType() {
		return fileType;
	}
	public void setFileType(String fileSype) {
		this.fileType = fileSype;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	@Override
	public String toString() {
		return "MediaFiles [id=" + id + ", mediaId=" + mediaId + ", fileSize=" + fileSize
				+ ", fileType=" + fileType + ", filePath=" + filePath + "]";
	}
	
	
}
