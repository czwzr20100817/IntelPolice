package com.model;

import java.util.Date;

public class Files {
    //文件编号
    private String id;
    //文件名称
    private String name;
    //文件路径(相对路径)
    private String path;
    //文件描述
    private String description;
    //文件大小
    private String sizes;
    //上传时间
    private Date createTime;
    //文件类型
    private String type;
    //排序号
    private String sortCode;
    //状态
    private String status;
    //上传者
    private String userName;
    //同步过来的财物ID
    private String objId;
    //涉案财物id
    private String spoilsId;

    public Files() {
    }

    public Files(String id, String name, String path, String description, String sizes, Date createTime, String type, String sortCode, String status, String userName, String objId, String spoilsId) {
        this.id = id;
        this.name = name;
        this.path = path;
        this.description = description;
        this.sizes = sizes;
        this.createTime = createTime;
        this.type = type;
        this.sortCode = sortCode;
        this.status = status;
        this.userName = userName;
        this.objId = objId;
        this.spoilsId = spoilsId;
    }

    @Override
    public String toString() {
        return "Files{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", path='" + path + '\'' +
                ", description='" + description + '\'' +
                ", sizes='" + sizes + '\'' +
                ", createTime='" + createTime + '\'' +
                ", type='" + type + '\'' +
                ", sortCode='" + sortCode + '\'' +
                ", status='" + status + '\'' +
                ", userName='" + userName + '\'' +
                ", objId='" + objId + '\'' +
                ", spoilsId='" + spoilsId + '\'' +
                '}';
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getSizes() {
        return sizes;
    }

    public void setSizes(String sizes) {
        this.sizes = sizes;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSortCode() {
        return sortCode;
    }

    public void setSortCode(String sortCode) {
        this.sortCode = sortCode;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getObjId() {
        return objId;
    }

    public void setObjId(String objId) {
        this.objId = objId;
    }

    public String getSpoilsId() {
        return spoilsId;
    }

    public void setSpoilsId(String spoilsId) {
        this.spoilsId = spoilsId;
    }
}
