package com.model;

import java.util.Date;

/**
 * 笔录问题类
 *
 */
public class RecordQuestion {
    private String id;              //主键QUESTIONID

    private String questionCode;  //问题编号QUESTIONCODE

    private String content;        //问题内容CONTENT  0询问 1讯问  2辩论

    private String type;           //问题类型TYPE

    private String caseType;      //案件类型CASETYPE

    private Date addTime;       //创建时间ADDTIME

    private String addUser;       //创建人ADDUSER

    private Date modifyTime;   //修改时间MODIFYTIME

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getQuestionCode() {
        return questionCode;
    }

    public void setQuestionCode(String questionCode) {
        this.questionCode = questionCode;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getCaseType() {
        return caseType;
    }

    public void setCaseType(String caseType) {
        this.caseType = caseType;
    }

    public Date getAddTime() {
        return addTime;
    }

    public void setAddTime(Date addTime) {
        this.addTime = addTime;
    }

    public String getAddUser() {
        return addUser;
    }

    public void setAddUser(String addUser) {
        this.addUser = addUser;
    }

    public Date getModifyTime() {
        return modifyTime;
    }

    public void setModifyTime(Date modifyTime) {
        this.modifyTime = modifyTime;
    }
}
