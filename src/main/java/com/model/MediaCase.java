package com.model;

/**
 * 执法音视频关联案件表EA_MEDIA_CASE
 * @author Administrator
 *
 */
public class MediaCase {
	/**
	 * 音视频关联ID	MEDIACASEID	VARCHAR2(32)	√	
音视频ID	MEDIAID	VARCHAR2(32)	√	
案件ID	CASEID	VARCHAR2(32)	√	
	 */
	
	private String mediaCaseId;							  //音视频关联ID
	private String mediaId;								  //音视频记录ID
	private String caseId;								  //案件ID
	public String getMediaCaseId() {
		return mediaCaseId;
	}
	public void setMediaCaseId(String mediaCaseId) {
		this.mediaCaseId = mediaCaseId;
	}
	public String getMediaId() {
		return mediaId;
	}
	public void setMediaId(String mediaId) {
		this.mediaId = mediaId;
	}
	public String getCaseId() {
		return caseId;
	}
	public void setCaseId(String caseId) {
		this.caseId = caseId;
	}
	@Override
	public String toString() {
		return "MediaCase [mediaCaseId=" + mediaCaseId + ", mediaId=" + mediaId + ", caseId=" + caseId + "]";
	}
	
	
}
