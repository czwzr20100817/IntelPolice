package com.model.bean;

import com.model.InvolvedCase;

public class InvolvedCaseBean extends InvolvedCase {
    private String storeId; //仓库ID
    private String lockersId;   //柜子ID
    private String boxNo;   //箱子编号

    public String getStoreId() {
        return storeId;
    }

    public void setStoreId(String storeId) {
        this.storeId = storeId;
    }

    public String getLockersId() {
        return lockersId;
    }

    public void setLockersId(String lockersId) {
        this.lockersId = lockersId;
    }

    public String getBoxNo() {
        return boxNo;
    }

    public void setBoxNo(String boxNo) {
        this.boxNo = boxNo;
    }
}
