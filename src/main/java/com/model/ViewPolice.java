package com.model;

public class ViewPolice {

    int id;
    char name;
    char idNum;
    char workNumber;
    int organizationId;
    char mobile;

    public ViewPolice() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public char getName() {
        return name;
    }

    public void setName(char name) {
        this.name = name;
    }

    public char getIdNum() {
        return idNum;
    }

    public void setIdNum(char idNum) {
        this.idNum = idNum;
    }

    public char getWorkNumber() {
        return workNumber;
    }

    public void setWorkNumber(char workNumber) {
        this.workNumber = workNumber;
    }

    public int getOrganizationId() {
        return organizationId;
    }

    public void setOrganizationId(int organizationId) {
        this.organizationId = organizationId;
    }

    public char getMobile() {
        return mobile;
    }

    public void setMobile(char mobile) {
        this.mobile = mobile;
    }

    @Override
    public String toString() {
        return "ViewPolice{" +
                "id=" + id +
                ", name=" + name +
                ", idNum=" + idNum +
                ", workNumber=" + workNumber +
                ", organizationId=" + organizationId +
                ", mobile=" + mobile +
                '}';
    }
}
